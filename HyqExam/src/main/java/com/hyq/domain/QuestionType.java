package com.hyq.domain;

/**
 * 问题 类型实体
 * @author HYQ
 *
 */
public class QuestionType {
	private Integer  id;  //类型id
	private String name;  //类型名称
	
	
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
	
	

}
